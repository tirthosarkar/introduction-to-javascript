/**
 * variable ==> vary + able
 * vary ==> change
 * able ==> can do
 * variable ==> can change
 * variable ==> a value that can change
 * 
 * 1. variable is a container that holds a value.
 * 2. variable can be changed during the program execution.
 * 3. variable can be of different types: number, string, boolean, etc.
 * 4. variable can be declared using var, let, or const keywords in JavaScript.
 * 5. variable can be assigned a value using the assignment operator (=).
 * 6. variable can be used to store user input, perform calculations, and control the flow of the program.
 * 7. variable names should be meaningful and follow naming conventions (e.g., camelCase).
 * 8. variable can be global (accessible throughout the program) or local (accessible only within a specific block or function).
 *   
 * 5 things to remember about variable:
 */

var weight = 70; // variable declaration and initialization
console.log(weight); // Output: 70
weight = 75; // variable reassignment
console.log(weight); // Output: 75